using System;

class FloatingPointTypes
{
    static void Main()
    {
        double doubleFirst = 34.567839023;
        float floatNumber = 12.345f;
        double doubleSecond = 8923.1234567;
        float floatSecond = 3456.091f;
        Console.WriteLine("{0}\n{1}\n{2}\n{3}", doubleFirst, floatNumber, doubleSecond, floatSecond);
    }
}
